package day05_matematikselIslemler;

public class C02_Pre_Post_Increment {
    public static void main(String[] args) {
        int sayi = 10;
        System.out.println("pre-inc :" + ++sayi); // 11

        System.out.println("post-inc : " + sayi++); // 11

        System.out.println("post-inc'den sonra : " + sayi); //12


    }

}
